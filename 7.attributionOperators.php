<?php
/*
    Operadores de atribuição

    como utilizar operadores aritméticos e melhorar a 
    codificação de expressões matemáticas com PHP

    *Operadores: += -= /= *= %=
*/

$itemA = 25;

$itemA += 25;
echo $itemA . PHP_EOL;

$itemA -= 5;
echo $itemA . PHP_EOL;

$itemA /= 3;
echo $itemA . PHP_EOL;

$itemA *= 9;
echo $itemA . PHP_EOL;

$itemA %= 2;
echo $itemA . PHP_EOL;